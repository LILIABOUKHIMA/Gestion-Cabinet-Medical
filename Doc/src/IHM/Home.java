    private void bValidateRDVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bValidateRDVActionPerformed
        int index = tableRDV.getSelectedRow();

        try {
            Patient patient = (Patient) cbPatients.getSelectedItem();
            String dateRD = utils.DateUtil.getDateAsString(dcDateRDV.getDate());
            String datePR = utils.DateUtil.getDateAsString(dcDatePriseRDV.getDate());

            rendezVous.setIdPatient(patient.getId());
            rendezVous.setDatePR(datePR);
            rendezVous.setDateRD(dateRD);

            if (rendezVous.getId() == 0) {
                rendezVous.Ajouter();

                listRDVs.add(rendezVous);
                DefaultTableModel model = (DefaultTableModel) tableRDV.getModel();
                model.addRow(getRowRdv(rendezVous, patient));
            } else {
                rendezVous.Modifier();

                listRDVs.set(index, rendezVous);

                DefaultTableModel model = (DefaultTableModel) tableRDV.getModel();
                model.setValueAt(rendezVous.getDateRD().toString(), index, 0);
                model.setValueAt(patient.getNom(), index, 0);
                model.setValueAt(patient.getPrenom(), index, 0);
                model.setValueAt(patient.getTel(), index, 0);
                model.setValueAt(rendezVous.getDatePR().toString(), index, 0);
            }

            emptyAllFormRDV();
            disabledAllFormRDV();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_bValidateRDVActionPerformed

