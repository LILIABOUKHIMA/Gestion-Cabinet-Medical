    private void updateListPatients() {
        String filtre = tfFiltreRecherche.getText().trim();
        
        String condition = "1";
        if (! filtre.equals("")){
            condition  = " nom like '%"+filtre+"%' ";
            condition += "OR  prenom like '%"+filtre+"%' ";
            condition += "OR  adresse like '%"+filtre+"%' ";
            condition += "OR  age like '%"+filtre+"%' ";
            condition += "OR  tel like '%"+filtre+"%' ";
        }
        
        listPatients = Patient.rechercher( condition );

        DefaultTableModel model = (DefaultTableModel) client_tab.getModel();
        model.setRowCount(0);

        cbPatients.removeAllItems();

        for (Patient patient : listPatients) {
            Vector row = new Vector();

            row.add(patient.getId());
            row.add(patient.getNom());
            row.add(patient.getPrenom());
            row.add(patient.getAdresse());
            row.add(patient.getAge());
            row.add(patient.getGS());
            row.add(patient.getTel());
            row.add(patient.getTaill());
            row.add(patient.getPoids());

            row.add(patient);//Extra fffff

            model.addRow(row);

            cbPatients.addItem(patient);
        }
    }

