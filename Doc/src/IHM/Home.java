    private void updateListRDV() {
        String dateDuJour = utils.DateUtil.getDateOfThisDay();
        listRDVs = RendezVous.rechercher(" dateRD >= '" + dateDuJour + "' ORDER BY dateRD ASC");

        DefaultTableModel model = (DefaultTableModel) tableRDV.getModel();
        model.setRowCount(0);

        for (RendezVous rdv : listRDVs) {
            model.addRow(getRowRdv(rdv));
        }
    }

    private static Vector getRowRdv(RendezVous rdv) {
        Patient patient = Patient.getById(rdv.getIdPatient());
        return getRowRdv(rdv, patient);
    }

    private static Vector getRowRdv(RendezVous rdv, Patient patient) {
        Vector row = new Vector();

        row.add(rdv.getDateRD().toString());
        row.add(patient.getNom());
        row.add(patient.getPrenom());
        row.add(patient.getTel());
        row.add(rdv.getDatePR());

        return row;
    }

